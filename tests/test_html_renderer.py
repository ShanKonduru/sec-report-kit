def test_render_consolidated_dashboard_date_filter_excludes_old_html_files(tmp_path):
    """HTML report files outside the date range must not appear as 'available'."""
    reports_dir = tmp_path / "reports"
    output_dir = reports_dir
    reports_dir.mkdir()

    # Write a bandit HTML file and backdate its mtime to 2000-01-01
    bandit_html = reports_dir / "bandit-report.html"
    bandit_html.write_text("<html>bandit</html>", encoding="utf-8")
    old_mtime = dt.datetime(2000, 1, 1, tzinfo=dt.timezone.utc).timestamp()
    import os
    os.utime(bandit_html, (old_mtime, old_mtime))

    # Write a trivy HTML file with a very-future mtime (3000-01-01)
    trivy_html = reports_dir / "trivy-report.html"
    trivy_html.write_text("<html>trivy</html>", encoding="utf-8")
    future_mtime = dt.datetime(3000, 1, 1, tzinfo=dt.timezone.utc).timestamp()
    os.utime(trivy_html, (future_mtime, future_mtime))

    tz = dt.timezone.utc
    since = dt.datetime(2026, 1, 1, tzinfo=tz)
    until = dt.datetime(2026, 12, 31, 23, 59, 59, tzinfo=tz)

    html = render_consolidated_dashboard_report(
        target_ref="repo",
        findings=[],
        counts={"CRITICAL": 0, "HIGH": 0, "MEDIUM": 0, "LOW": 0, "UNKNOWN": 0},
        reports_dir=reports_dir,
        output_dir=output_dir,
        modified_since=since,
        modified_until=until,
    )

    # Bandit HTML is from 2000 (before since) — must NOT be shown as available
    assert 'src="../reports/bandit-report.html"' not in html
    assert 'src="bandit-report.html"' not in html
    # Trivy HTML is from 3000 (after until) — must NOT be shown as available
    assert 'src="trivy-report.html"' not in html
    # Since and until labels should appear in the hero
    assert "Since: 2026-01-01" in html
    assert "Until: 2026-12-31" in html


